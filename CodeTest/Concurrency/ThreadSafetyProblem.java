import java.util.concurrent.TimeUnit;

/**
 * =============================================================================
 * 线程安全问题示例 - 展示竞态条件
 * =============================================================================
 *
 * 本示例演示了多线程环境下最核心的问题：线程安全问题和竞态条件。
 *
 * 【什么是竞态条件（Race Condition）】
 * 竞态条件是指多个线程同时访问共享资源时，由于执行顺序的不确定性，
 * 导致最终结果依赖于线程执行的时序。
 *
 * 【为什么会产生线程安全问题】
 * 1. 多个线程同时访问共享资源（如实例变量、全局变量）
 * 2. 至少有一个线程在修改（写）该资源
 * 3. 读-修改-写 操作不是原子性的
 *
 * 【典型的问题场景】
 * 1. 自增/自减操作：i++ 看似简单，实际包含三个CPU指令：
 *    - 读取i的值到寄存器
 *    - 寄存器值+1
 *    - 写回内存
 *
 * 2. 检查后执行（Check-Then-Act）：
 *    if (balance >= amount) {  // 检查
 *        balance = balance - amount;  // 执行
 *    }
 *
 * 【本示例的核心问题】
 * 银行账户操作：存款 +100，取款 -50
 * 预期：1000 + 10*100 - 10*50 = 1500
 * 但由于竞态条件，最终余额往往不是1500
 *
 * =============================================================================
 */
public class ThreadSafetyProblem {

    // =============================================================================
    // 共享资源 - 银行账户余额
    // =============================================================================
    // 注意：这个变量被多个线程共享访问
    // - 10个存款线程都会读取和修改它
    // - 10个取款线程都会读取和修改它
    // - 没有任何同步保护措施
    //
    // 【为什么静态变量是共享的？】
    // - 静态变量存储在堆内存中
    // - 所有线程共享同一个Java堆
    // - 因此所有线程都能访问同一个accountBalance变量
    // =============================================================================
    private static int accountBalance = 1000;

    /**
     * =============================================================================
     * main方法 - 程序入口
     * =============================================================================
     *
     * 本方法创建20个线程来同时操作同一个银行账户：
     * - 10个存款线程，每个存100元
     * - 10个取款线程，每个取50元
     *
     * 【预期结果】
     * 最终余额 = 初始1000 + 存款1000（10*100）- 取款500（10*50）= 1500元
     *
     * 【实际结果】
     * 由于竞态条件，最终余额往往不是1500元，而是各种奇怪的数字！
     *
     * @param args 命令行参数
     * @throws InterruptedException 当线程被中断时抛出
     */
    public static void main(String[] args) throws InterruptedException {
        System.out.println("=== 线程安全问题示例 ===");
        System.out.println("初始账户余额: " + accountBalance);
        System.out.println("预期最终余额: 1000 + 10*100 - 10*50 = 1500 元");
        System.out.println();
        System.out.println("开始20个线程并发操作...");
        System.out.println("【注意观察输出，最终余额很可能不是1500！】");
        System.out.println();

        // =================================================================
        // 创建线程数组
        // =================================================================
        // 10个存款线程 + 10个取款线程 = 20个线程同时操作
        Thread[] depositThreads = new Thread[10];
        Thread[] withdrawThreads = new Thread[10];

        // =================================================================
        // 启动存款线程
        // =================================================================
        // 每个存款线程都会调用deposit(100)方法
        // 理论上：10个线程 × 100元 = 1000元
        //
        // 【问题分析】
        // 这10个线程会同时：
        // 1. 读取accountBalance的值
        // 2. 计算新余额（currentBalance + 100）
        // 3. 写入新余额
        //
        // 但这些操作不是原子的！会出现以下问题：
        // - 线程A读取余额1000
        // - 线程B读取余额1000（还没来得及写入）
        // - 线程A写入1100
        // - 线程B也写入1100（覆盖了线程A的结果）
        // - 结果：少加了100元！
        // =================================================================
        for (int i = 0; i < 10; i++) {
            // 使用Lambda表达式创建线程
            depositThreads[i] = new Thread(() -> {
                // 存款100元
                deposit(100);
            }, "存款线程-" + (i+1));
            depositThreads[i].start();
        }

        // =================================================================
        // 启动取款线程
        // =================================================================
        // 每个取款线程都会调用withdraw(50)方法
        // 理论上：10个线程 × 50元 = 500元
        //
        // 【问题分析 - 更严重！】
        // 除了上述的读-写问题，还有：
        // 1. 余额不足的检查可能不准确
        //    - 线程A检查余额>=50（假设为1000）
        //    - 线程B检查余额>=50（还是1000）
        //    - 线程A取走50，余额变为950
        //    - 线程B取走50，余额变为900
        //    - 但如果中间又有存款操作介入...
        //
        // 2. 负数余额问题
        //    - 线程A检查余额>=50（假设为100）
        //    - 线程B也检查余额>=50（还是100）
        //    - 线程A取走50，余额变为50
        //    - 线程B取走50，余额变为0（正常）
        //    - 但如果有多个取款线程同时通过检查...
        // =================================================================
        for (int i = 0; i < 10; i++) {
            withdrawThreads[i] = new Thread(() -> {
                // 取款50元
                withdraw(50);
            }, "取款线程-" + (i+1));
            withdrawThreads[i].start();
        }

        // =================================================================
        // 等待所有线程完成
        // =================================================================
        // 使用join()确保主线程等待所有子线程完成后再继续
        // 否则主线程可能直接打印结果，而子线程还在执行
        for (Thread t : depositThreads) {
            t.join();
        }
        for (Thread t : withdrawThreads) {
            t.join();
        }

        // =================================================================
        // 打印最终结果
        // =================================================================
        System.out.println();
        System.out.println("========================================");
        System.out.println("最终账户余额: " + accountBalance);
        System.out.println("预期余额: 1500 元");
        System.out.println("差异: " + (1500 - accountBalance) + " 元");
        System.out.println("========================================");
        System.out.println();
        System.out.println("【问题分析】");
        System.out.println("线程安全问题导致余额不正确！");
        System.out.println("这是因为多个线程同时读写共享变量，");
        System.out.println("而read-modify-write操作不是原子性的。");
    }

    /**
     * =============================================================================
     * 存款操作 - 存在严重的线程安全问题
     * =============================================================================
     *
     * 这个方法演示了一个典型的线程安全问题：读-修改-写（Read-Modify-Write）
     *
     * 【问题根源】
     * deposit操作看似简单：accountBalance = accountBalance + amount
     * 但实际上包含三个独立的CPU操作：
     * 1. READ：从内存读取accountBalance到CPU寄存器
     * 2. MODIFY：在CPU寄存器中进行加法运算
     * 3. WRITE：将计算结果写回内存
     *
     * 【问题场景】
     * 假设初始余额=1000，存款100：
     *
     * 正确情况（串行执行）：
     * T1: READ(1000) → MODIFY(1100) → WRITE(1100) → 完成
     * T2: READ(1100) → MODIFY(1200) → WRITE(1200) → 完成
     * 最终：1200
     *
     * 错误情况（并发执行）：
     * T1: READ(1000) ──────────────────→ MODIFY(1100) ──→ WRITE(1100)
     * T2: READ(1000) → MODIFY(1100) → WRITE(1100) ──────────────────→ 完成
     * 最终：1100（丢失了T1的存款！）
     *
     * 【为什么要加sleep？】
     * sleep(10) 模拟业务处理时间（如网络通信、数据库操作）
     * 这会加剧竞态条件的发生，让问题更容易复现
     * 在实际生产环境中，业务处理时间越长，竞态条件越容易发生！
     *
     * @param amount 存款金额（正数）
     */
    private static void deposit(int amount) {
        // =================================================================
        // 第一步：模拟业务处理（如验证账户信息）
        // =================================================================
        // 这个sleep增加了两个读操作之间的时间窗口
        // 使得其他线程更容易在这个窗口内插入操作
        // 从而更容易触发竞态条件
        try {
            TimeUnit.MILLISECONDS.sleep(10);
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
        }

        // =================================================================
        // 第二步：读取当前余额（READ）
        // =================================================================
        // 【问题点1】其他线程可能同时在执行这里！
        // 如果多个线程同时读取，它们可能读到相同的旧值
        int currentBalance = accountBalance;
        System.out.println(Thread.currentThread().getName()
            + " [存款] 读取余额: " + currentBalance);

        // =================================================================
        // 第三步：模拟业务处理（如记录日志、发送通知）
        // =================================================================
        // 又是一个时间窗口，让其他线程有更多机会介入
        try {
            TimeUnit.MILLISECONDS.sleep(10);
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
        }

        // =================================================================
        // 第四步：更新余额（WRITE）
        // =================================================================
        // 【问题点2】这是典型的非原子操作！
        // accountBalance = currentBalance + amount 包含：
        // - 读取accountBalance（但这行代码已经做过了）
        // - 计算 currentBalance + amount
        // - 写入accountBalance
        //
        // 【关键问题】
        // 在当前线程sleep期间，其他线程可能已经修改了accountBalance
        // 但当前线程使用的是旧的currentBalance值进行计算
        // 导致其他线程的修改被覆盖（丢失更新）
        accountBalance = currentBalance + amount;
        System.out.println(Thread.currentThread().getName()
            + " [存款] " + amount + " 元，更新后余额: " + accountBalance);
    }

    /**
     * =============================================================================
     * 取款操作 - 存在严重的线程安全问题
     * =============================================================================
     *
     * 这个方法除了读-修改-写问题，还有额外的检查-执行（Check-Then-Act）问题！
     *
     * 【检查-执行（Check-Then-Act）问题】
     * 代码中的 if (currentBalance >= amount) 看似简单，
     * 但这个检查和后续的取款操作之间存在时间窗口，
     * 在这个窗口内，其他线程可能：
     * - 取走了钱，导致余额不足
     * - 存入了钱，导致余额充足
     *
     * 【问题场景】
     * 假设余额=100，取款50：
     *
     * 正确情况：
     * T1: READ(100) → CHECK(100>=50, true) → 等待处理 → WRITE(50)
     * T2: READ(50) → CHECK(50>=50, true) → 等待处理 → WRITE(0)
     * 最终：0
     *
     * 错误情况（竞态）：
     * T1: READ(100) → CHECK(100>=50, true) ──→ [其他线程介入] ──→ WRITE(50)
     * T2: READ(100) → CHECK(100>=50, true) → WRITE(50) ──→ [其他线程介入] ──→ 完成
     * 最终：50（而不是0，因为T2覆盖了T1的结果）
     *
     * 【更严重的问题：余额变成负数】
     * 如果检查和取款之间有足够的时间窗口，
     * 多个取款线程可能都通过检查，然后都执行取款
     * 导致余额变成负数！
     *
     * @param amount 取款金额（正数）
     */
    private static void withdraw(int amount) {
        // =================================================================
        // 第一步：模拟业务处理（如验证密码、检查额度）
        // =================================================================
        try {
            TimeUnit.MILLISECONDS.sleep(10);
        } catch (InterruptedException e) {
            Thread.currentThread().interrupt();
        }

        // =================================================================
        // 第二步：读取当前余额（READ）
        // =================================================================
        // 【问题点1】同存款操作，可能读到过期数据
        int currentBalance = accountBalance;
        System.out.println(Thread.currentThread().getName()
            + " [取款] 读取余额: " + currentBalance);

        // =================================================================
        // 第三步：检查余额是否足够（CHECK）
        // =================================================================
        // 【问题点2】这是典型的检查-执行（Check-Then-Act）模式！
        // 检查和后续的取款操作之间存在时间窗口
        // 在这个窗口内，其他线程可能修改余额
        // 导致检查结果失效
        //
        // 【为什么会失效？】
        // 假设当前余额=100，amount=50
        // - T1线程检查：100 >= 50 ✓
        // - T2线程检查：100 >= 50 ✓
        // - T1正在等待（sleep期间）
        // - T3线程存款100，余额变为200
        // - T1继续执行，取走50，余额变为150
        // - T2继续执行，取走50，余额变为100
        // - 两次取款都"成功"了，但实际上应该更早被阻止！
        if (currentBalance >= amount) {
            // =================================================================
            // 第四步：模拟业务处理（如等待人工审批）
            // =================================================================
            // 又一个时间窗口，其他线程可能已经修改了余额
            try {
                TimeUnit.MILLISECONDS.sleep(10);
            } catch (InterruptedException e) {
                Thread.currentThread().interrupt();
            }

            // =================================================================
            // 第五步：执行取款（WRITE）
            // =================================================================
            // 【问题点3】基于过期数据计算！
            // currentBalance是之前读取的值
            // 在sleep期间，其他线程可能已经修改了accountBalance
            // 但我们不知道，仍然使用旧的currentBalance进行计算
            //
            // 【示例】
            // - T1读取余额=100，currentBalance=100
            // - T2读取余额=100，currentBalance=100
            // - T1通过检查，等待
            // - T2通过检查，等待
            // - T1执行：accountBalance = 100 - 50 = 50
            // - T2执行：accountBalance = 100 - 50 = 50（错误！应该基于50而不是100）
            accountBalance = currentBalance - amount;
            System.out.println(Thread.currentThread().getName()
                + " [取款] " + amount + " 元，更新后余额: " + accountBalance);
        } else {
            // 当余额不足时的处理
            System.out.println(Thread.currentThread().getName()
                + " [取款] 余额不足，取款失败");
        }
    }

    // =============================================================================
    // 扩展知识点
    // =============================================================================
    /**
     * 【线程安全的三大问题】
     *
     * 1. 竞态条件（Race Condition）
     *    - 定义：执行结果依赖于线程的执行顺序
     *    - 原因：多个线程同时访问共享资源
     *    - 典型场景：read-modify-write、check-then-act
     *
     * 2. 内存可见性（Memory Visibility）
     *    - 定义：一个线程对共享变量的修改，对其他线程不可见
     *    - 原因：CPU缓存、JIT编译器优化
     *    - 典型场景：变量没有volatile修饰
     *
     * 3. 对象发布逸态（Object Publication Escape）
     *    - 定义：对象在未完全构造时被其他线程访问
     *    - 原因：构造函数中this引用逸出
     *    - 典型场景：构造函数中启动线程、注册监听器
     *
     * =================================================================
     *
     * 【如何解决线程安全问题】
     *
     * 1. 原子性保护
     *    - 使用synchronized关键字
     *    - 使用java.util.concurrent.atomic包中的原子类
     *    - 使用volatile（仅保证可见性，不保证原子性）
     *
     * 2. 锁机制
     *    - synchronized（内置锁/监视器锁）
     *    - ReentrantLock（可重入锁）
     *    - ReadWriteLock（读写锁）
     *    - StampedLock（乐观读锁）
     *
     * 3. 无锁算法
     *    - CAS（Compare-And-Swap）操作
     *    - java.util.concurrent.atomic中的原子类
     *
     * 4. 不可变对象
     *    - 对象状态不可修改
     *    - 所有字段用final修饰
     *    - 没有setter方法
     *
     * =================================================================
     *
     * 【思考题】
     *
     * 1. 为什么sleep会加剧竞态条件？
     *    - sleep延长了每个操作的时间窗口
     *    - 增加了其他线程插入的机会
     *    - 实际上，任何耗时的业务操作都会加剧问题
     *
     * 2. 如果accountBalance是局部变量，还会有线程安全问题吗？
     *    - 不会！因为局部变量存储在栈内存中
     *    - 每个线程有独立的栈，不共享局部变量
     *    - 这就是"线程栈隔离"的概念
     *
     * 3. 为什么实际生产环境中线程安全问题更难发现？
     *    - 测试环境通常是单线程或低并发
     *    - 生产环境可能有大量并发请求
     *    - 问题只在特定时序下才会触发
     *    - 被称为"海兹定律"：只在高并发时才会暴露的问题
     *
     * 4. 如何确保资金不会丢失或出错？
     *    - 使用数据库事务（ACID特性）
     *    - 使用分布式锁
     *    - 使用乐观锁（版本号或CAS）
     *    - 设计上避免并发修改同一账户
     */
}